def ask(
    messages: list, max_tokens: int = 300, temperature: float = 0.3
) -> tuple[str, int, float]:
    """Один вызов модели. Возвращает (ответ, токены_выхода, секунды)."""
    t0 = time.time()
    resp = client.chat.completions.create(
        model=MODEL,
        messages=messages,
        temperature=temperature,
        max_tokens=max_tokens,
    )
    return (
        resp.choices[0].message.content.strip(),
        resp.usage.completion_tokens,
        time.time() - t0,
    )


def show(label: str, answer: str, tokens: int, secs: float) -> None:
    print(f"\n{'─' * 70}\n {label}\n{'─' * 70}")
    print(answer)
    print(f"\n [{tokens} токенов, {secs:.1f} с]")


