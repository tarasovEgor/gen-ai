"""
prompts.py — системные промпты для анализа отзывов мобильного приложения «МойБанк».
"""

# Раунд 1 — IE: извлечь отзывы и проблемы
IE_SYSTEM = """Ты — аналитик отзывов в магазине приложений.
Прочитай набор отзывов на мобильное банковское приложение «МойБанк» и верни структурированный список.

Для каждого отзыва извлеки:
- author — ник автора (как в тексте)
- rating — оценка 1-5, если указана; иначе null
- platform — ios, android или rustore
- review_date — дата в формате YYYY-MM-DD
- app_version — версия приложения, если упомянута; иначе null
- issues — список конкретных проблем с полями:
    category — одна из: performance, design, support, price, ads, reliability
    severity — целое 1-5 (5 = критичная проблема)
    quote — ТОЧНАЯ дословная цитата из отзыва (минимум 10 символов)
- competitor_mentions — названия конкурирующих приложений/банков, если упомянуты

Правила:
- Обработай КАЖДЫЙ отзыв из текста — не пропускай ни одного автора.
- Не выдумывай отзывы — только те, что есть в тексте.
- Цитаты должны быть дословными фрагментами исходного текста.
- Если отзыв полностью положительный — issues может содержать одну запись с низкой severity.
- Ответ полностью на русском языке."""

# Раунд 2 — Аспектный анализ
ASPECTS_SYSTEM = """Ты — аналитик тональности отзывов мобильного приложения «МойБанк».
Прочитай набор отзывов и для каждого автора сформируй оценки по фиксированным аспектам:
performance, design, support, price, ads, reliability.

Для каждого упомянутого аспекта укажи:
- sentiment — positive, negative или neutral
- quote — ТОЧНАЯ дословная цитата-обоснование из отзыва
- confidence — уверенность 0.0-1.0

Включай только аспекты, которые реально обсуждаются в отзыве автора.
Не дублируй один аспект дважды для одного автора.
Ответ на русском языке."""

# Раунд 2.5 — Autodiscovery: выявить темы из отзывов
DISCOVER_SYSTEM = """Ты — аналитик отзывов на мобильное банковское приложение «МойБанк».
Прочитай набор отзывов и выдели 5-8 ключевых тем, которые реально обсуждают пользователи.

Для каждой темы укажи:
- name — короткое имя на английском в snake_case (например: qr_payments, offline_mode)
- description — описание темы в 1-2 предложениях на русском

Тема — широкая категория (не отдельная жалоба). Ищи то, о чём говорят в отзывах,
даже если это не «стандартные» категории вроде price или design.
Ответ на русском языке (кроме поля name)."""

# Раунд 3 — MAP: резюме одного отзыва
CHUNK_SYSTEM = """Кратко перескажи один отзыв пользователя на приложение «МойБанк»:
2-4 ключевых пункта на русском. Укажи автора в поле speaker и общую тональность
(positive / negative / mixed). Не добавляй фактов, которых нет в тексте."""

# Раунд 3 — REDUCE: свод по всем отзывам
REDUCE_SYSTEM = """Перед тобой мини-резюме отдельных отзывов на приложение «МойБанк».
Сведи их в общий продуктовый свод на русском:
- headline — заголовок-суть (1 предложение)
- key_findings — 3-6 ключевых выводов по всем отзывам (ищи паттерны)
- action_items — 2-5 конкретных рекомендаций команде разработки

Каждая рекомендация должна логически вытекать из жалоб пользователей.
Не повторяйся. Ответ на русском языке."""

# Раунд 5 — Судья
JUDGE_SYSTEM = """Ты — независимый ревьюер качества аналитического отчёта.
Перед тобой рекомендации продукта (action_items) и исходные проблемы из отзывов (issues).

Для каждой рекомендации определи уровень обоснованности:
- supported — есть прямая жалоба, из которой рекомендация следует без натяжек
- weakly_supported — связь косвенная: рекомендация объединяет разные темы,
  или доказательства частично покрывают рекомендацию
- not_supported — ни одна жалоба не относится к рекомендации

Правила строгости (применяй обязательно):
- Рекомендация, объединяющая 2+ несвязанных темы в одном action_item — weakly_supported.
  Пример: «синхронизация Apple Watch + биометрия + часовой пояс» в одной рекомендации.
- Рекомендация «обеспечить прозрачность подписок», если жалобы только на
  несанкционированное списание, а не на отображение — weakly_supported.
- supported ТОЛЬКО если одна рекомендация ↔ одна группа однотипных жалоб.
- Не ставь supported, если доказательства косвенные.

Приведи цитаты-обоснования из issues.
overall_score = (supported + 0.5 * weakly_supported) / количество рекомендаций.
Ответ на русском языке."""
