"""
Раунд 5.5 — Стресс-тест retry: когда механика бессильна
=======================================================
После раунда 5 у нас есть `make_client()` с автоматическими ретраями. На
обычной задаче он вытягивает 49-50 персон из 50. Возникает соблазн думать,
что retry — серебряная пуля: «поставлю max_retries побольше, и всё
догонится».

Это не так. Здесь мы намеренно создаём ситуацию, в которой retry не
поможет НИКОГДА: промпт и схема противоречат друг другу.

Выводы:
  - Retry правит случайные сбои модели, не системные.
  - Если SYSTEM_PROMPT говорит одно, а схема требует другое — каждый retry
    будет приносить ту же ошибку, только дороже.
  - «Не работает — увеличу max_retries» = деньги в трубу.
  - Правильное действие: править либо промпт, либо схему, чтобы они
    согласовались. Это и есть prompt-schema alignment.

Запуск:
  python stress_test.py
"""

import time
from typing import Literal

from llm_client import get_model, make_client
from pydantic import BaseModel, Field

client = make_client()
MODEL = get_model()


# ───── Конфликт №1: «мягкий» — числовое противоречие ─────
# Промпт явно говорит: «возраст любой от 18 до 100».
# Схема разрешает только 18-25. Современные модели часто умеют
# «угадать» правильный ответ из JSON Schema, которую llm_client
# подкладывает в системный промпт.
CONFLICTING_PROMPT = (
    "Сгенерируй персону покупателя. ВАЖНО: возраст должен быть любым "
    "правдоподобным от 18 до 100 лет. Без ограничений сверху."
)


class TooYoungPersona(BaseModel):
    name: str
    age: int = Field(ge=18, le=25)  # схема требует молодёжь
    city: str


# ───── Конфликт №2: «жёсткий» — невыполнимое требование ─────
# Имя должно быть длиннее 200 символов. Это противоречит самому
# понятию ФИО: модель физически не может сгенерировать такое имя,
# не написав абсурдный текст.
IMPOSSIBLE_NAME_PROMPT = (
    "Сгенерируй персону покупателя — обычное русское ФИО, обычный возраст."
)


class ImpossibleNamePersona(BaseModel):
    # Требуем имя длиной от 200 до 500 символов — никакое реальное ФИО
    # не подходит. Модель будет писать «Анна Петрова», Pydantic будет
    # ругаться, retry будет тратить токены.
    name: str = Field(min_length=200, max_length=500)
    age: int = Field(ge=18, le=75)


# ───── Конфликт №3: «жёсткий» — Literal с вымышленным значением ─────
# Промпт говорит «крупный реальный российский город», схема разрешает
# только «Урюпинск» (несуществующий). Если модель честно следует
# промпту — пишет «Москва» и валится.
ABSURD_CITY_PROMPT = (
    "Сгенерируй персону. В поле city укажи КРУПНЫЙ РЕАЛЬНЫЙ "
    "российский город (Москва, Санкт-Петербург, Новосибирск и т.п.). "
    "Не используй вымышленные названия."
)


class AbsurdCityPersona(BaseModel):
    name: str
    age: int = Field(ge=18, le=75)
    city: Literal["Урюпинск"]


def stress(label: str, prompt: str, model_cls, max_retries: int):
    print(f"\n━━━ {label} (max_retries={max_retries}) ━━━")
    print(f"Промпт: {prompt[:80]}…")
    print(f"Схема: {model_cls.__name__}")
    t0 = time.time()
    try:
        obj = client.chat.completions.create(
            model=MODEL,
            messages=[
                {"role": "system", "content": prompt},
                {"role": "user", "content": "Создай одну персону."},
            ],
            response_model=model_cls,
            max_retries=max_retries,
            temperature=0.9,
        )
        dt = time.time() - t0
        print(f"  ✓ за {dt:.1f}c: {obj}")
    except Exception as e:
        dt = time.time() - t0
        # max_retries+1 запросов на запрос. Конкретные числа зависят от модели,
        # но порядок — десятки секунд и токенов на каждый «провал».
        print(f"  ✗ за {dt:.1f}c, потратили ~{max_retries + 1} запросов")
        print(f"    {type(e).__name__}: {str(e)[:200]}")


def main():
    print(f"Модель: {MODEL}")
    print("Гипотеза студентов: «увеличу max_retries — заработает».")
    print("Проверим на трёх уровнях жёсткости.\n")

    # ── Уровень 1. Мягкий конфликт ──
    # Современные модели часто «читают» жёсткую границу из JSON Schema
    # и подгоняются под неё, даже когда основной промпт говорит другое.
    # Тогда retry не нужен и тест успешен «случайно». Это тоже важное
    # наблюдение: модель доверяет схеме больше, чем естественному
    # языку промпта.
    print("━━━ Уровень 1: мягкое противоречие («возраст 18-100» vs schema 18-25) ━━━")
    stress("max_retries=1", CONFLICTING_PROMPT, TooYoungPersona, max_retries=1)
    stress("max_retries=3", CONFLICTING_PROMPT, TooYoungPersona, max_retries=3)
    stress("max_retries=5", CONFLICTING_PROMPT, TooYoungPersona, max_retries=5)

    # ── Уровень 2. Жёсткий конфликт: невыполнимое требование ──
    # Модель физически не может выдать русское ФИО длиной 200+ символов.
    # Здесь retry гарантированно бессилен — она будет писать «Анна»
    # снова и снова, и Pydantic будет отбрасывать.
    print("\n━━━ Уровень 2: невыполнимое требование (имя 200-500 символов) ━━━")
    stress(
        "max_retries=1", IMPOSSIBLE_NAME_PROMPT, ImpossibleNamePersona, max_retries=1
    )
    stress(
        "max_retries=3 (втрое больше токенов — тот же результат)",
        IMPOSSIBLE_NAME_PROMPT,
        ImpossibleNamePersona,
        max_retries=3,
    )

    # ── Уровень 3. Промпт прямо запрещает то, что требует схема ──
    # city: Literal["Урюпинск"] и промпт «крупный реальный город».
    # Любой ответ нарушает что-то. Модель должна выбрать сторону —
    # и обычно выбирает промпт, потому что инструкция в нём прямее.
    print(
        '\n━━━ Уровень 3: промпт vs схема, кто кого («реальный город» vs Literal["Урюпинск"]) ━━━'
    )
    stress("max_retries=3", ABSURD_CITY_PROMPT, AbsurdCityPersona, max_retries=3)

    print("\n━━━ Мораль ━━━")
    print("Уровень 1: модель часто «вытягивает» через JSON Schema. Случайное везение.")
    print("Уровень 2: невыполнимое требование — retry бессилен. Деньги в трубу.")
    print("Уровень 3: модель сама выбирает, кому верить — промпту или схеме.")
    print()
    print("Лекарство во всех случаях — не max_retries, а согласование:")
    print("  1) Промпт описывает то же, что и схема (перечисление, диапазон).")
    print("  2) Схема не требует физически невозможного.")
    print("  3) Few-shot с правильными примерами закрывает «зазор».")
    print("В лекции это prompt-schema alignment.")


if __name__ == "__main__":
    main()
